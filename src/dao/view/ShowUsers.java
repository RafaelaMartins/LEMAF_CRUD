    private void bttEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttEditActionPerformed
        int row = 0;
        String value = "";
        if(tblUsers.isEditing()){
            row = tblUsers.getEditingRow();
            ControllerCargos cargo = new ControllerCargos();
            nameGender = tblUsers.getModel().getValueAt(row, 3).toString();
            String idCargo = cargo.selectCode(tblUsers.getModel().getValueAt(row, 3).toString());
            this.select.setId(tblUsers.getModel().getValueAt(row, 0).toString());
            this.select.setName(tblUsers.getModel().getValueAt(row, 1).toString());
            this.select.setCpf(tblUsers.getModel().getValueAt(row, 2).toString());
            this.select.setCargo(idCargo);
            this.select.setBorn(tblUsers.getModel().getValueAt(row, 4).toString());
            this.select.setGender(tblUsers.getModel().getValueAt(row, 5).toString());
            this.select.setDate_origin(tblUsers.getModel().getValueAt(row, 6).toString());
            UpdateUsers update = new UpdateUsers();
            setDefaultCloseOperation(update.EXIT_ON_CLOSE);
            update.setVisible(true);
            reboot();
            
        }else if (tblUsers.getSelectedRow()>= row){
            row = tblUsers.getSelectedRow();
            ControllerCargos cargo = new ControllerCargos();
            String idCargo = cargo.selectCode(tblUsers.getModel().getValueAt(row, 3).toString());
            this.select.setId(tblUsers.getModel().getValueAt(row, 0).toString());
            this.select.setName(tblUsers.getModel().getValueAt(row, 1).toString());
            this.select.setCpf(tblUsers.getModel().getValueAt(row, 2).toString());
            this.select.setCargo(idCargo);
            this.select.setBorn(tblUsers.getModel().getValueAt(row, 4).toString());
            this.select.setGender(tblUsers.getModel().getValueAt(row, 5).toString());
            this.select.setDate_origin(tblUsers.getModel().getValueAt(row, 6).toString());
            UpdateUsers update = new UpdateUsers();
            update.setVisible(true);
            reboot();
        }else{
        System.out.println(tblUsers.getSelectedRow());
        reboot();
        }
        reboot();
    }//GEN-LAST:event_bttEditActionPerformed

